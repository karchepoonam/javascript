while (k >= 40) {
  console.log(k);
  k--;
}

// WAP To print first 15 odd numbers
console.log("3. To print first 15 odd numbers");

let j = 0; // initialization
while (j <= 15) {
  // condition
  if (j % 2 == 1) {
    console.log(j);
  }
  j++; // update expression
}

